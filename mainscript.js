function checkAll(formname, checktoggle) {
    "use strict";
    var checkboxes = [],
        i;
    checkboxes = document[formname].getElementsByTagName('input');
    for (i = 0; i < checkboxes.length; i += 1) {
        if (checkboxes[i].type === 'checkbox') {
            checkboxes[i].checked = checktoggle;
        }
    }
}
